n1= float(input('Digite o numero X: '))
n2= float(input('Digite o numero Y: '))
#terminal do usuário
print('_____________________________________________________________________________')
soma = n1 + n2
print('Adição: {} + {} = {}'.format(n1,n2,soma))
menos = n1 - n2
print('Subtração: {} - {} = {}'.format(n1,n2,menos))
vezes = n1 * n2
print('Multiplicação: {} X {} = {:.2f}'.format(n1,n2,vezes))
Dividir = n1 / n2
print('Divisão: {} / {} = {:.2f}'.format(n1,n2,Dividir))
print('_____________________________________________________________________________')


